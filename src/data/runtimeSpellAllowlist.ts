function normalizeSpellName(value: unknown): string {
  return String(value || '').trim().replace(/\s+/g, ' ').toLowerCase()
}

function stripYamlQuotes(value: string): string {
  const trimmed = value.trim()
  if (
    (trimmed.startsWith('"') && trimmed.endsWith('"')) ||
    (trimmed.startsWith("'") && trimmed.endsWith("'"))
  ) {
    return trimmed.slice(1, -1)
  }
  return trimmed
}

export function parseRuntimeSpellNames(raw?: string): Set<string> {
  const names = new Set<string>()
  if (raw === undefined) {
    for (const name of RUNTIME_SUPPORTED_SPELL_NAMES) {
      names.add(normalizeSpellName(name))
    }
    return names
  }

  for (const line of raw.split(/\r?\n/)) {
    const match = line.match(/^\s*-\s+name:\s*(.+?)\s*$/)
    if (!match) continue
    const name = normalizeSpellName(stripYamlQuotes(match[1] ?? ''))
    if (name) names.add(name)
  }
  return names
}

export const runtimeSpellNames = parseRuntimeSpellNames()

export function isRuntimeSpellSupported(name: unknown): boolean {
  return runtimeSpellNames.has(normalizeSpellName(name))
}

export function filterRuntimeSupportedSpells<T extends { name: string }>(spells: readonly T[]): T[] {
  return spells.filter(spell => isRuntimeSpellSupported(spell.name))
}
