function toSpellId(value: string): string {
  return spellIdSet.has(value) ? value : spellIdByName.get(value) ?? value
}

function normalizeSpellList(values: readonly string[]): string[] {
  return values.map(toSpellId)
}

export function applyDnd5eBlogLocalization(slug: string, character: CharacterData): CharacterData {
  const text = dnd5eBlogZhText[slug]
  const spellOverrides = spellSelectionOverrides[slug] ?? {}
  const localized: CharacterData = {
    ...character,
    ...(text
      ? {
          personalityTraits: text.personalityTraits,
          ideals: text.ideals,
          bonds: text.bonds,
          flaws: text.flaws,
          backstory: text.bio,
          age: text.age,
          height: text.height,
          weight: text.weight,
          eyes: text.eyes,
          hair: text.hair,
          skin: text.skin,
          allies: text.allies,
          treasure: text.treasure,
        }
      : {}),
    ...spellOverrides,
  }

  return {
    ...localized,
    cantrips: normalizeSpellList(localized.cantrips),
    spellsKnown: normalizeSpellList(localized.spellsKnown),
    spellsPrepared: normalizeSpellList(localized.spellsPrepared),
  }
}
