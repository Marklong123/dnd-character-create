/**
 * Translate a game term for display. Returns the original if no translation exists.
 */
export type GameTermCategory = 'weapon' | 'armor' | 'spell' | 'school' | 'damageType' | 'pack' | 'background' | 'class' | 'race' | 'subrace' | 'skill' | 'proficiency' | 'feature' | 'trait' | 'language' | 'subclass' | 'equipment' | 'ability' | 'casterType' | 'size'
export type GameDescriptionCategory = 'classFeature' | 'backgroundFeature' | 'spell'

function normalizeId(name: string): string {
  return name.trim().toLowerCase().replace(/['’]/g, '').replace(/-/g, ' ')
}

function kebabize(name: string): string {
  return normalizeId(name).replace(/\s+/g, '-')
}

function titleize(name: string): string {
  return name.split(/[-\s]/).map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ')
}

function compactKey(name: string): string {
  return normalizeId(name).replace(/\s+/g, '')
}

function termCandidates(name: string): string[] {
  const trimmed = name.trim()
  const normalized = normalizeId(trimmed)
  const kebab = kebabize(trimmed)
  const title = titleize(trimmed)
  const normalizedTitle = titleize(normalized)
  const deApostrophe = trimmed.replace(/['’]/g, '')
  const lower = trimmed.toLowerCase()
  const lowerKebab = lower.replace(/\s+/g, '-')
  return [
    trimmed,
    title,
    normalizedTitle,
    normalized,
    kebab,
    lower,
    lowerKebab,
    deApostrophe,
    titleize(deApostrophe),
  ].filter((value, index, values) => value && values.indexOf(value) === index)
}

function lookupTerm(table: Record<string, string>, name: string): string | undefined {
  for (const candidate of termCandidates(name)) {
    const translated = table[candidate]
    if (translated) return translated
  }

  const wanted = compactKey(name)
  return Object.entries(table).find(([key]) => compactKey(key) === wanted)?.[1]
}

function lookupAnyTerm(name: string, tables: Array<Record<string, string>>): string | undefined {
  for (const table of tables) {
    const translated = lookupTerm(table, name)
    if (translated) return translated
  }
  return undefined
}

export function translateGameDescription(
  key: string,
  fallback: string,
  locale: string,
  category: GameDescriptionCategory,
): string {
  if (!locale.startsWith('zh')) return fallback
  switch (category) {
    case 'classFeature':
      return classFeatureDescriptionsZh[key] ?? '这项特性会根据原始规则提供对应的职业效果。'
    case 'backgroundFeature':
      return backgroundFeatureDescriptionsZh[key] ?? '该背景特性会根据原始规则提供对应的剧情、社交或探索优势。'
    case 'spell':
      return spellDescriptionsZh[key] ?? '该法术产生魔法效果；具体数值、限制和豁免按原始规则处理。'
    default:
      return fallback
  }
}

export function translateRulesText(text: string, locale: string): string {
  if (!locale.startsWith('zh')) return text
  if (rulesTextZh[text]) return rulesTextZh[text]
  return text
    .replace(/(\d+)\s+feet/g, '$1 尺')
    .replace(/(\d+)-foot/g, '$1 尺')
    .replace(/Self \(([^)]+)\)/g, '自身（$1）')
    .replace(/Concentration, up to ([^,]+)/g, '专注，至多 $1')
    .replace(/\bcone\b/g, '锥形')
    .replace(/\bcube\b/g, '立方')
    .replace(/\bradius\b/g, '半径')
    .replace(/\bsphere\b/g, '球形')
}

export function translateGameTerm(
  name: string,
  locale: string,
  category: GameTermCategory,
  variant?: string,
): string {
  if (locale.startsWith('zh')) {
    switch (category) {
      case 'weapon':
        return lookupTerm(weaponNamesZh, name) ?? name
      case 'armor':
        return lookupTerm(armorNamesZh, name) ?? name
      case 'spell':
        return lookupTerm(spellNamesZh, name) ?? name
      case 'school':
        return lookupTerm(spellSchoolsZh, name) ?? name
      case 'damageType':
        return lookupTerm(damageTypesZh, name) ?? name
      case 'pack':
        return lookupTerm(equipmentPacksZh, name) ?? name
      case 'background':
        return lookupTerm(backgroundNamesZh, name) ?? name
      case 'class':
        return lookupTerm(classNamesZh, name) ?? name
      case 'race':
        return lookupTerm(raceNamesZh, name) ?? name
      case 'subrace':
        return lookupAnyTerm(name, [subraceNamesZh, subclassNamesZh]) ?? name
      case 'skill':
        return lookupTerm(skillNamesZh, name) ?? name
      case 'proficiency':
        return lookupAnyTerm(name, [proficiencyNamesZh, weaponNamesZh, armorNamesZh, equipmentNamesZh]) ?? name
      case 'feature':
        return lookupAnyTerm(name, [featureNamesZh, traitNamesZh, subclassNamesZh]) ?? name
      case 'trait':
        return lookupAnyTerm(name, [traitNamesZh, featureNamesZh, subclassNamesZh]) ?? name
      case 'language':
        return lookupTerm(languageNamesZh, name) ?? name
      case 'subclass':
        return lookupTerm(subclassNamesZh, name) ?? name
      case 'equipment':
        return lookupAnyTerm(name, [equipmentNamesZh, proficiencyNamesZh, weaponNamesZh, armorNamesZh, equipmentPacksZh]) ?? name
      case 'ability':
        return abilityNamesZh[name.toLowerCase()] ?? name.toUpperCase()
      case 'casterType':
        return lookupTerm(casterTypeNamesZh, name) ?? name
      case 'size':
        return lookupTerm(sizeNamesZh, name) ?? name
      default:
        return name
    }
  }

  if (category === 'ability') return name.toUpperCase()
  if (category === 'casterType' || category === 'size') return name

  if (locale !== 'it') return name

  switch (category) {
    case 'weapon':
      return weaponNamesIt[name] ?? name
    case 'armor':
      return armorNamesIt[name] ?? name
    case 'spell':
      return spellNamesIt[name] ?? name
    case 'school':
      return spellSchoolsIt[name] ?? name
    case 'damageType':
      return damageTypesIt[name] ?? name
    case 'pack':
      return equipmentPacksIt[name] ?? name
    case 'background': {
      if (backgroundNamesIt[name]) return backgroundNamesIt[name]
      // Try capitalizing each word (IDs may be lowercase, e.g. "folk-hero" → "Folk Hero")
      const bgCapitalized = titleize(name)
      return backgroundNamesIt[bgCapitalized] ?? name
    }
    case 'class': {
      // Variant-specific dicts use lowercase IDs; standard dict uses capitalized English names.
      // Try both forms to handle either input style.
      const lowered = name.toLowerCase()
      const capitalized = name.charAt(0).toUpperCase() + name.slice(1)
      if (variant === 'brancalonia') {
        return brancaloniaClassNamesIt[lowered] ?? brancaloniaClassNamesIt[name] ?? classNamesIt[capitalized] ?? classNamesIt[name] ?? name
      }
      if (variant === 'apocalisse') {
        return apocalisseClassNamesIt[lowered] ?? apocalisseClassNamesIt[name] ?? classNamesIt[capitalized] ?? classNamesIt[name] ?? name
      }
      return classNamesIt[capitalized] ?? classNamesIt[name] ?? name
    }
    case 'race': {
      if (raceNamesIt[name]) return raceNamesIt[name]
      // Try capitalizing each word (IDs may be hyphenated, e.g. "half-elf" → "Half-Elf")
      const raceCapitalized = name.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('-')
      return raceNamesIt[raceCapitalized] ?? raceNamesIt[raceCapitalized.replace(/-/g, ' ')] ?? name
    }
    case 'subrace': {
      if (subraceNamesIt[name]) return subraceNamesIt[name]
      const subCapitalized = titleize(name)
      return subraceNamesIt[subCapitalized] ?? name
    }
    case 'skill': {
      if (skillNamesIt[name]) return skillNamesIt[name]
      const skillCapitalized = titleize(name)
      return skillNamesIt[skillCapitalized] ?? name
    }
    case 'proficiency': {
      if (proficiencyNamesIt[name]) return proficiencyNamesIt[name]
      // Individual weapon/armor proficiencies come in lowercase (e.g. "hand crossbow", "light armor")
      const profCapitalized = titleize(name)
      return proficiencyNamesIt[profCapitalized] ?? weaponNamesIt[profCapitalized] ?? armorNamesIt[profCapitalized] ?? name
    }
    case 'feature':
      return featureNamesIt[name] ?? name
    case 'trait':
      return traitNamesIt[name] ?? name
    case 'language':
      return languageNamesIt[name] ?? name
    case 'subclass':
      return subclassNamesIt[name] ?? name
    case 'equipment':
      return equipmentNamesIt[name] ?? name
    default:
      return name
  }
}
